ipcMain.handle('get-app-version', () => {
  return app.getVersion();
});

ipcMain.handle('get-app-name', () => {
  return app.getName();
});

// Handle menu actions
ipcMain.on('menu-action', (event, action) => {
  console.log('Menu action:', action);
});
